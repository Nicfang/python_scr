def getProxyIp(n):
    proxy = []
    for i in range(1,n):
        try:
            url = 'http://www.xicidaili.com/nn/'+str(i)
            req = urllib2.Request(url,headers=headers)
            res = urllib2.urlopen(req).read()
            soup = BeautifulSoup(res)
            ips = soup.findAll('tr')
            for x in range(1,len(ips)):
                ip = ips[x]
                tds = ip.findAll("td")
                ip_temp = tds[1].contents[0]+" "+tds[2].contents[0]+" "+tds[5].contents[0]
                proxy.append(ip_temp)
        except:
            continue
    return proxy

